import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const localeRoot = path.join(root, 'src', 'i18n', 'locales');
const flagsRoot = path.join(root, 'src', 'assets', 'flags');
const generatedRoot = path.join(root, 'shared', 'generated');
const registryPath = path.join(generatedRoot, 'i18nRegistry.ts');
const electronTextPath = path.join(generatedRoot, 'electronText.ts');
const defaultLanguage = 'zh-TW';
const fallbackLanguage = 'en';
const checkOnly = process.argv.includes('--check');

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function stableString(value) {
  return JSON.stringify(value, null, 2);
}

function writeOrCheck(filePath, content) {
  if (checkOnly) {
    const current = fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf8') : '';
    if (current !== content) {
      const relative = path.relative(root, filePath);
      throw new Error(`${relative} is stale. Run npm run i18n:generate-registry.`);
    }
    return;
  }

  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, 'utf8');
}

function flattenObject(value, prefix = '') {
  if (value && typeof value === 'object' && !Array.isArray(value)) {
    return Object.entries(value).flatMap(([key, child]) => (
      flattenObject(child, prefix ? `${prefix}.${key}` : key)
    ));
  }
  return [[prefix, value]];
}

function getValue(value, key) {
  return key.split('.').reduce((current, part) => (
    current && typeof current === 'object' && Object.prototype.hasOwnProperty.call(current, part)
      ? current[part]
      : undefined
  ), value);
}

function assertMeta(folderName, meta) {
  const requiredStrings = ['code', 'nativeName', 'englishName', 'flag', 'direction'];
  for (const key of requiredStrings) {
    if (typeof meta[key] !== 'string' || meta[key].trim() === '') {
      throw new Error(`${folderName}/meta.json missing string field: ${key}`);
    }
  }
  if (meta.code !== folderName) {
    throw new Error(`${folderName}/meta.json code must match folder name`);
  }
  if (meta.direction !== 'ltr' && meta.direction !== 'rtl') {
    throw new Error(`${folderName}/meta.json direction must be "ltr" or "rtl"`);
  }
  if (meta.enabled !== true) {
    throw new Error(`${folderName}/meta.json enabled must be true for bundled locales`);
  }
  if (!Array.isArray(meta.aliases) || meta.aliases.some(alias => typeof alias !== 'string')) {
    throw new Error(`${folderName}/meta.json aliases must be an array of strings`);
  }
  const flagPath = path.join(flagsRoot, `${meta.flag}.svg`);
  if (!fs.existsSync(flagPath)) {
    throw new Error(`${folderName}/meta.json references missing flag: src/assets/flags/${meta.flag}.svg`);
  }
}

function loadLocales() {
  if (!fs.existsSync(localeRoot)) return [];

  const collator = new Intl.Collator('en', { numeric: true, sensitivity: 'base' });
  return fs.readdirSync(localeRoot, { withFileTypes: true })
    .filter(entry => entry.isDirectory())
    .map((entry) => {
      const folder = entry.name;
      const metaPath = path.join(localeRoot, folder, 'meta.json');
      const translationPath = path.join(localeRoot, folder, 'translation.json');
      if (!fs.existsSync(metaPath)) throw new Error(`${folder} is missing meta.json`);
      if (!fs.existsSync(translationPath)) throw new Error(`${folder} is missing translation.json`);
      const meta = readJson(metaPath);
      assertMeta(folder, meta);
      return {
        code: meta.code,
        nativeName: meta.nativeName,
        englishName: meta.englishName,
        flag: meta.flag,
        direction: meta.direction,
        enabled: meta.enabled,
        aliases: meta.aliases,
      };
    })
    .sort((a, b) => collator.compare(a.code, b.code));
}

function buildRegistry(locales) {
  const localeCodes = locales.map(locale => locale.code);
  const aliasEntries = [];
  for (const locale of locales) {
    for (const alias of [locale.code, ...locale.aliases]) {
      aliasEntries.push([alias.toLowerCase(), locale.code]);
    }
  }
  aliasEntries.sort((a, b) => a[0].localeCompare(b[0], 'en'));

  return `// This file is generated by scripts/generate-i18n-registry.mjs. Do not edit by hand.

export const LOCALE_REGISTRY = ${stableString(locales)} as const;

export const SUPPORTED_LANGUAGES = ${stableString(localeCodes)} as const;

export const LOCALE_ALIAS_MAP = ${stableString(Object.fromEntries(aliasEntries))} as const;
`;
}

function buildElectronText(locales) {
  const catalogs = Object.fromEntries(locales.map(locale => [
    locale.code,
    readJson(path.join(localeRoot, locale.code, 'translation.json')),
  ]));
  const defaultElectron = Object.fromEntries(flattenObject(catalogs[defaultLanguage]?.electron ?? {}));
  const fallbackElectron = Object.fromEntries(flattenObject(catalogs[fallbackLanguage]?.electron ?? {}));
  const keys = [...new Set([...Object.keys(defaultElectron), ...Object.keys(fallbackElectron)])].sort();
  const text = {};

  for (const locale of locales) {
    const localeElectron = Object.fromEntries(flattenObject(catalogs[locale.code]?.electron ?? {}));
    text[locale.code] = Object.fromEntries(keys.map(key => [
      key,
      localeElectron[key] ?? fallbackElectron[key] ?? defaultElectron[key] ?? key,
    ]));
  }

  return `// This file is generated by scripts/generate-i18n-registry.mjs. Do not edit by hand.

export const ELECTRON_TEXT = ${stableString(text)} as const;

export type ElectronTextKey = keyof typeof ELECTRON_TEXT['${fallbackLanguage}'];
`;
}

const locales = loadLocales();
if (!locales.some(locale => locale.code === defaultLanguage)) {
  throw new Error(`Missing default locale: ${defaultLanguage}`);
}
if (!locales.some(locale => locale.code === fallbackLanguage)) {
  throw new Error(`Missing fallback locale: ${fallbackLanguage}`);
}

writeOrCheck(registryPath, buildRegistry(locales));
writeOrCheck(electronTextPath, buildElectronText(locales));

if (!checkOnly) {
  console.log(`Generated i18n registry for ${locales.map(locale => locale.code).join(', ')}`);
}
